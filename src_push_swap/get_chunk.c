#include "push_swap.h"

int	get_chunk(int n)
{
	if (n <= 5)
		return (n);
	if (n <= 10)
		return (5);
	if (n <= 50)
		return (7);
	if (n <= 150)
		return (6);
	if (n <= 550)
		return (11);
	return (10);
}
