#include "push_swap.h"

void	ra(t_list **a)
{
	t_list	*ptr;

	if (a == NULL || (*a) == NULL)
		return ;
	ptr = (*a);
	ft_lstadd_back(a, ft_lstnew(ptr->n));
	ft_lstdel_front(a);
}
