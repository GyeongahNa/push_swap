#include "push_swap.h"

void	rb(t_list **b)
{
	t_list *ptr;

	if (b == NULL || (*b) == NULL)
		return ;
	ptr = (*b);
	ft_lstadd_back(b, ft_lstnew(ptr->n));
	ft_lstdel_front(b);
}
